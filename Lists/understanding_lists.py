"""
Lists are used to store multiple items in a single variable.

Lists are one of 4 built-in data types in Python used to store collections of data, the other 3 are Tuple, Set, and Dictionary, all with different qualities and usage.

Lists are created using square brackets:

Just like array in other languages, we have lists in python which can store different datatypes
"""
names = ['John', 'Bob', 'Mosh', 'Sarah', 'Mary']

print(names)

print(names[0]) # print the first element in the lists
print(names[3])
print(names[-1]) # prints the last name on the list
print(names[-2])

print(names[1: 3]) # you can use : to create range and access the names in the list
print(names[2: ])


